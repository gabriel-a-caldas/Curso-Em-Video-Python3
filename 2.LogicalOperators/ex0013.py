# Faça um programa que leia um valor em graus celsius
# e converta o valor para fahrenheit

temperauraCelsius = int(input('Informe a temperatura em graus celsius: '))

temperaturaFarenheit = (temperauraCelsius*1.8)+32

print(f'A temperatura {temperauraCelsius}ºC em fahrenheit equivale a {temperaturaFarenheit}ºF')