# Desenvolva um programa que leia as
# duas notas de um aluno, calcule e mostre a sua média

notaUm = int(input("Insira a sua primeira nota: "))
notaDois = int(input("Insira a sua segunda nota: "))

media = (notaUm + notaDois)/2

print(f'Você tirou: {notaUm} e {notaDois}, logo, sua média é: {media}')